const fs = require('fs');
const path = require('path');
const guidesPath = path.join(__dirname, '../data/guides.json');
const guides = JSON.parse(fs.readFileSync(guidesPath, 'utf8'));

const findIdx = id => guides.findIndex(g => g.id === id);

guides[findIdx('weapon-tier-list')].content += `\n\n## 武器精炼材料获取\n四星武器的精炼需要重复获取同一把武器。活动赠送武器的精炼材料通常可以通过活动获取，确保满精。抽卡获取的四星武器精炼则需要运气或大量抽数。锻造武器可以多次锻造来精炼，是最稳定的精炼方式。\n\n## 总结\n武器选择的核心是匹配角色定位。输出角色追求攻击力和暴击，辅助角色追求充能和生命值。不必过度追求五星武器，满精四星武器在大多数场景下完全够用。合理规划武器资源，优先保证主力角色有好武器使用。`;

guides[findIdx('reroll-guide-detailed')].content += `\n\n## 多平台注意事项\n异环支持多个平台，刷初始时需要注意平台差异。部分平台可能有账号注册限制或更严格的反刷机制。建议选择最方便重置账号的平台进行刷初始操作。跨平台数据互通的情况也需要提前确认，避免刷到好开局却无法在其他平台使用。\n\n## 刷初始的道德考量\n虽然刷初始在技术上是允许的，但过度刷初始（使用自动化脚本等）可能违反游戏服务条款。建议手动进行刷初始操作，每次间隔适当时间，避免触发反作弊系统。正常的刷初始行为不会受到惩罚，但使用第三方工具可能会面临封号风险。`;

guides[findIdx('performance-guide')].content += `\n\n## 音频设置优化\n音频处理也会消耗一定的系统资源。如果你的设备性能紧张，可以尝试降低音效质量或关闭部分音效。但建议至少保留基础音效——战斗中的音效提示对于判断敌人攻击时机非常重要，完全静音会影响游戏体验。\n\n## 云游戏选项\n如果你的设备性能不足以流畅运行异环，可以考虑使用云游戏服务。云游戏通过网络将游戏画面传输到你的设备，对本地硬件要求很低。缺点是需要稳定的网络连接，且可能有输入延迟。对于网络条件好的玩家，云游戏是一个不错的备选方案。\n\n## 定期重启\n长时间运行游戏后可能出现内存泄漏导致的卡顿。建议每隔2-3小时重启一次游戏客户端，释放累积的内存占用。这是一个简单但有效的维护措施。`;

guides[findIdx('event-guides')].content += `\n\n## 活动中的隐藏奖励\n部分活动存在隐藏奖励或彩蛋内容。这些内容通常需要特定操作才能触发，如在不被发现的情况下完成潜行任务、在限定时间内完成挑战等。隐藏奖励的提示通常藏在NPC对话或环境细节中，细心观察可以发现更多内容。\n\n## 活动效率最大化\n每个活动的投入产出比不同。一般来说，战斗类活动的体力效率最高（提供大量材料），剧情类活动提供独特的体验和抽卡货币。建议在活动初期集中完成战斗类内容获取材料，后期再慢慢享受剧情内容。\n\n## 活动错过后的补救\n如果实在无法参与某个活动，不要太过遗憾。游戏会定期推出新的活动，而且部分高人气活动的机制可能会在后续版本以新形式回归。关注社区动态可以获取未来活动的预告信息。`;

guides[findIdx('talent-priority')].content += `\n\n## 升级时机建议\n天赋升级的时机也很重要。建议在角色等级达到当前突破上限后再开始投入天赋升级材料。低等级角色的天赋升级收益不如等级提升明显，等到角色基础属性达标后再升级天赋可以获得更好的整体效果。\n\n## 天赋等级对战斗的影响\n天赋等级每提升一级，技能的伤害倍率或增益数值大约提升5-8%。虽然单级提升不大，但累计10级的提升非常可观。核心天赋从1级升到10级大约可以提升50-80%的效果，这是不容忽视的战力提升。\n\n## 培养完成标准\n一个角色的天赋培养到什么程度算完成？一般标准是：核心天赋8级，次要天赋6级。如果你想追求极致，核心天赋可以升到10级甚至更高，但8级之后每级的材料消耗急剧增加，性价比下降。`;

